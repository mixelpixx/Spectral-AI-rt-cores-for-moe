# Main build procedure
# =============================================================================

def main() -> None:
    print("=" * 70)
    print("  SpectralAI Ternary Expert — Split Build for Windows")
    print("  CUDA 13.2 CCCL / PyTorch 2.11 cu128 header isolation")
    print("=" * 70)

    # -------------------------------------------------------------------------
    # 0. Validate paths
    # -------------------------------------------------------------------------
    missing = []
    for label, path in [
        ("vcvarsall.bat", VCVARSALL),
        ("nvcc.exe",      str(NVCC)),
        ("CUDA include",  str(CUDA_INC)),
        ("PyTorch include", str(TORCH_INC)),
        ("Python include",  str(PYTHON_INC)),
    ]:
        if not Path(path).exists():
            missing.append(f"  {label}: {path}")

    if missing:
        print("\n[ERROR] The following required paths were not found:")
        for m in missing:
            print(m)
        print("\nEdit the PATHS section at the top of this script.")
        sys.exit(1)

    # -------------------------------------------------------------------------
    # 1. Create build directory and write generated sources
    # -------------------------------------------------------------------------
    BUILD_DIR.mkdir(parents=True, exist_ok=True)

    print(f"\n[STAGE 0] Writing generated sources to {BUILD_DIR}")
    KERNELS_CU.write_text(KERNELS_CU_SOURCE, encoding="utf-8")
    print(f"  Wrote: {KERNELS_CU}")
    WRAPPER_CPP.write_text(WRAPPER_CPP_SOURCE, encoding="utf-8")
    print(f"  Wrote: {WRAPPER_CPP}")

    # -------------------------------------------------------------------------
    # 2. Stage 1: nvcc compiles CUDA kernels → .obj
    #    No PyTorch headers. Use -DCCCL_IGNORE_MSVC_TRADITIONAL_PREPROCESSOR_WARNING
    #    to suppress the CCCL warning about /Zc:preprocessor.
    #    We compile device code only (no linking), producing a host+device .obj.
    # -------------------------------------------------------------------------
    print("\n[STAGE 1] nvcc: compile CUDA kernels (no PyTorch headers)")

    # Base nvcc flags — no PyTorch headers involved at all
    # --generate-code is the canonical way to set arch for nvcc; avoid mixing
    # -arch/-code with --generate-code as that can produce duplicate-code warnings.
    nvcc_base_flags = [
        str(NVCC),
        str(KERNELS_CU),
        "-c",                                # compile only, no link
        "-o", str(KERNELS_OBJ),
        "--generate-code", f"arch={COMPUTE_ARCH},code={SM_ARCH}",
        "-Xcompiler", "/EHsc",               # exception handling for MSVC host
        "-Xcompiler", "/MD",                 # match PyTorch's dynamic CRT
        "-Xcompiler", "/O2",
        # Suppress the CCCL warning that fires when /Zc:preprocessor is absent.
        # Our kernel file does NOT include Thrust/CCCL headers so this is safe.
        "-DCCCL_IGNORE_MSVC_TRADITIONAL_PREPROCESSOR_WARNING",
        "--keep-device-functions",           # expose device symbols to linker
        "-I", str(CUDA_INC),
        "-std=c++17",
        "--extended-lambda",
        "-lineinfo",                         # low-overhead debug info for nsight
    ]

    # --no-cuda-headers-in-path prevents nvcc from auto-prepending its own
    # CCCL/Thrust include dirs to the search path.  Available since nvcc ~12.4.
    # We try with it first; if nvcc rejects the flag we retry without it.
    nvcc_cmd_with_flag  = nvcc_base_flags + ["--no-cuda-headers-in-path"]
    nvcc_cmd_without    = nvcc_base_flags  # fallback

    def _try_nvcc(cmd: list[str], desc: str) -> bool:
        """Returns True on success, False on non-zero exit."""
        inner = " ".join(f'"{str(a)}"' if " " in str(a) else str(a) for a in cmd)
        full  = f'cmd.exe /C ""{VCVARSALL}" amd64 && {inner}"'
        print(f"\n--- {desc} ---")
        print(f"CMD: {full}\n")
        rc = subprocess.run(full, shell=True).returncode
        if rc == 0:
            print(f"[OK] {desc}")
        return rc == 0

    ok = _try_nvcc(nvcc_cmd_with_flag,
                   "nvcc: ternary_kernels_only.cu -> .obj (--no-cuda-headers-in-path)")
    if not ok:
        print("[WARN] --no-cuda-headers-in-path rejected by this nvcc — retrying without it")
        if not _try_nvcc(nvcc_cmd_without,
                         "nvcc: ternary_kernels_only.cu -> .obj (fallback)"):
            print("\n[FAILED] nvcc compilation failed on both attempts.")
            sys.exit(1)

    # -------------------------------------------------------------------------
    # 3. Stage 2: MSVC compiles pybind11 wrapper → .obj
    #    No nvcc involved. We include PyTorch + Python + pybind11 headers.
    #    /Zc:preprocessor is required by PyTorch 2.x but conflicts with CCCL —
    #    since we have zero CCCL/Thrust includes here, it is safe.
    # -------------------------------------------------------------------------
    print("\n[STAGE 2] MSVC: compile pybind11 wrapper (no nvcc)")

    # Collect include dirs
    includes = [
        f"/I{PYTHON_INC}",
        f"/I{TORCH_INC}",
        f"/I{TORCH_INC_TH}",
        f"/I{CUDA_INC}",
        f"/I{Path(TORCH_HOME) / 'include' / 'TH'}",
        f"/I{Path(TORCH_HOME) / 'include' / 'THC'}",
    ]

    # PyTorch 2.x requires these defines
    defines = [
        "/DTORCH_API_INCLUDE_EXTENSION_H",
        "/DTORCH_EXTENSION_NAME=ternary_expert_ext",
        "/D_GLIBCXX_USE_CXX11_ABI=0",       # matches PyTorch's ABI
        "/DNOMINMAX",
        "/D_USE_MATH_DEFINES",
    ]

    msvc_compile_cmd = [
        "cl.exe",
        "/nologo",
        "/EHsc",
        "/MD",                               # dynamic CRT (matches PyTorch)
        "/O2",
        "/std:c++17",
        "/Zc:preprocessor",                  # required by PyTorch 2.x headers
        "/W3",
        # Suppress C4100 (unreferenced formal parameter) — common in torch headers
        "/wd4100",
        # Suppress C4127 (conditional expression is constant) — common in torch macros
        "/wd4127",
        # Suppress C4244 (conversion warnings) — common in torch
        "/wd4244",
        *includes,
        *defines,
        "/c",                                # compile only
        str(WRAPPER_CPP),
        f"/Fo{WRAPPER_OBJ}",
    ]

    run_with_msvc(msvc_compile_cmd, "cl.exe: ternary_pybind_wrapper.cpp -> .obj")

    # -------------------------------------------------------------------------
    # 4. Stage 3: Link both .obj files → .pyd
    #    Use MSVC link.exe (not nvcc --link) to avoid re-introducing CCCL.
    # -------------------------------------------------------------------------
    print("\n[STAGE 3] link.exe: .obj + .obj -> .pyd")

    # PyTorch import libraries
    torch_libs = [
        str(TORCH_LIB / "torch.lib"),
        str(TORCH_LIB / "torch_cpu.lib"),
        str(TORCH_LIB / "torch_cuda.lib"),
        str(TORCH_LIB / "torch_python.lib"),
        str(TORCH_LIB / "c10.lib"),
        str(TORCH_LIB / "c10_cuda.lib"),
    ]

    # Filter to only libs that actually exist (torch_cuda.lib may be absent
    # on CPU-only builds, though we require CUDA here)
    torch_libs = [l for l in torch_libs if Path(l).exists()]

    # CUDA import libraries
    cuda_libs = [
        str(Path(CUDA_HOME) / "lib" / "x64" / "cuda.lib"),
        str(Path(CUDA_HOME) / "lib" / "x64" / "cudart.lib"),
    ]
    cuda_libs = [l for l in cuda_libs if Path(l).exists()]

    # Python import library
    python_lib_name = f"python{sys.version_info.major}{sys.version_info.minor}.lib"
    python_lib = str(PYTHON_LIB / python_lib_name)

    # Additional Windows system libs needed by torch
    sys_libs = ["kernel32.lib", "user32.lib", "advapi32.lib"]

    link_cmd = [
        "link.exe",
        "/nologo",
        "/DLL",                              # build a DLL (.pyd is a DLL)
        f"/OUT:{PYD_OUT}",
        f"/LIBPATH:{TORCH_LIB}",
        f"/LIBPATH:{PYTHON_LIB}",
        f"/LIBPATH:{Path(CUDA_HOME) / 'lib' / 'x64'}",
        str(KERNELS_OBJ),
        str(WRAPPER_OBJ),
        *torch_libs,
        *cuda_libs,
        python_lib,
        *sys_libs,
        "/SUBSYSTEM:WINDOWS",
        "/MACHINE:X64",
    ]

    run_with_msvc(link_cmd, f"link.exe: -> {PYD_OUT.name}")

    # -------------------------------------------------------------------------
    # 5. Verify the output
    # -------------------------------------------------------------------------
    if PYD_OUT.exists():
        size_kb = PYD_OUT.stat().st_size // 1024
        print(f"\n{'=' * 70}")
        print(f"  BUILD SUCCESS")
        print(f"  Output: {PYD_OUT}  ({size_kb} KB)")
        print(f"{'=' * 70}")
        print(f"\nTo use from Python:")
        print(f"  import sys")
        print(f'  sys.path.insert(0, r"{SCRIPT_DIR}")')
        print(f"  import {EXT_NAME} as ternary_ext")
        print(f"  # ternary_ext.pack_ternary(...)")
        print(f"  # ternary_ext.ternary_linear(...)")
        print(f"  # ternary_ext.ternary_gated_mlp(...)")
    else:
        print("\n[ERROR] .pyd not found after link step — check output above.")
        sys.exit(1)


