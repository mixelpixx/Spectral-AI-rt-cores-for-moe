def run_with_msvc(args: list[str], description: str) -> None:
    """
    Wraps a command in a cmd.exe call that first sources vcvarsall.bat (x64)
    so that cl.exe, link.exe and their libraries are on PATH.
    """
    # Escape each arg for cmd.exe: wrap in double quotes if it contains spaces
    def quote(s: str) -> str:
        return f'"{s}"' if " " in s else s

    inner = " ".join(quote(str(a)) for a in args)
    cmd = f'cmd.exe /C ""{VCVARSALL}" amd64 && {inner}"'

    print(f"\n--- {description} ---")
    print(f"CMD: {cmd}\n")

    result = subprocess.run(cmd, shell=True)
    if result.returncode != 0:
        print(f"\n[FAILED] {description} (exit code {result.returncode})")
        sys.exit(result.returncode)
    print(f"[OK] {description}")


def run(args: list[str], description: str) -> None:
    """Run a command directly (for nvcc which doesn't need vcvarsall when PATH is set)."""
    cmd = " ".join(f'"{str(a)}"' if " " in str(a) else str(a) for a in args)
    print(f"\n--- {description} ---")
    print(f"CMD: {cmd}\n")

    result = subprocess.run(cmd, shell=True)
    if result.returncode != 0:
        print(f"\n[FAILED] {description} (exit code {result.returncode})")
        sys.exit(result.returncode)
    print(f"[OK] {description}")


